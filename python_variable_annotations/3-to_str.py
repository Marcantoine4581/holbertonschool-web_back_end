#!/usr/bin/env python3
"""Basic annotations - to_string funtion"""


def to_str(n: float) -> str:
    """
    to_str:
        Type-annotated function to_str that takes a float n as argument
        and returns the string representation of the float.
    Args:
        n (float) : float number
    Return:
        A string.
    """
    return str(n)
